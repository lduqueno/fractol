#include "libft.h"

char	*ft_strdup(const char *src)
{
	unsigned int	i;
	char			*result;

	i = 0;
	if (!(result = (char *)malloc(sizeof(char) * (ft_strlen(src) + 1))))
		return (NULL);
	while (src[i])
	{
		result[i] = src[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
